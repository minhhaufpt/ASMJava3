    public boolean checkValidate() {

        try {
            double toan = Double.parseDouble(txttoan.getText());
            double li = Double.parseDouble(txtli.getText());
            double hoa = Double.parseDouble(txthoa.getText());
            if (toan <= 0 || toan > 10) {
                JOptionPane.showMessageDialog(this, "Điểm Toán trong khoảng từ 0.5 - 10", "Điểm", JOptionPane.INFORMATION_MESSAGE);
                txttoan.requestFocus();
                return false;
            }
            if (li <= 0 || li > 10) {
                JOptionPane.showMessageDialog(this, "Điểm Lí trong khoảng từ 0.5 - 10", "Điểm", JOptionPane.INFORMATION_MESSAGE);
                txtli.requestFocus();
                return false;
            }
            if (hoa <= 0 || hoa > 10) {
                JOptionPane.showMessageDialog(this, "Điểm Hóa trong khoảng từ 0.5 - 10", "Điểm", JOptionPane.INFORMATION_MESSAGE);
                txthoa.requestFocus();
                return false;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập điểm là số và dùng dấu . để phân cách phần thập phân", "ERROL", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
