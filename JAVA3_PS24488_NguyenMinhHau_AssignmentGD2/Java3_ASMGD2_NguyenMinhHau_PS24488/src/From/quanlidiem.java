            JOptionPane.showMessageDialog(this, "Mã số sinh viên không có dữ liệu. Không thể thực hiện thao tác.", "Delete", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(getClass().getResource("/asm/Image/nosuc.png")));
